
public class Print0To10UsingLoops {
    public static void main(String[] args) {

        // for loop:-

        for ( int i=0; i<11; i++){
            System.out.print(i+" ");
        }


        // while loop :-

        int i = 0;
        while(i<11){
            System.out.print(i+ " ");
            i++;
        }


        // do while:-

        // int i = 0;
        do{
            System.out.print(i + " ");
            i++;
        }while(i<11);
    }
}
